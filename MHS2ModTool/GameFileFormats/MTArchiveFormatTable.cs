        private static readonly FrozenDictionary<uint, string> s_HashToExtensionMap;
        private static readonly FrozenDictionary<string, uint> s_ExtensionToHashMap;

        static MTArchiveFormatTable()
        {
            var hashToExtensionMap = new Dictionary<uint, string>();
            var extensionToHashMap = new Dictionary<string, uint>();

            foreach ((var name, var ext) in s_FormatTable)
            {
                uint hash = HashUtils.CalculateCrc32NoSign(name);

                hashToExtensionMap.Add(hash, ext);
                extensionToHashMap.Add(ext, hash);
            }

            s_HashToExtensionMap = hashToExtensionMap.ToFrozenDictionary();
            s_ExtensionToHashMap = extensionToHashMap.ToFrozenDictionary();
        }

